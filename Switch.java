    public class Switch {  
    public static void main(String[] args) {  
        char number='a';
        switch(number){  
        case 'a': System.out.println("10");  
        break;
        case 'b': System.out.println("20");  
          break;
        case 'c': System.out.println("30");  
        break;  
        default:System.out.println("Not in 10, 20 or 30");  
        }  
        }  
    }  
